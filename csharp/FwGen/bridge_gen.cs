    private static void GeneratePacketCodecCs(string root, FwConfig config, string schemaDir)
    {
        var output = config.BridgePacketCodecCsPath(root);
        var schema = ProtoSchema.ParseFiles(SchemaFiles(schemaDir));
        var rootNamespace = TextUtil.PascalName(config.ProjectName());
        var text = new StringBuilder();
        text.AppendLine("// @generated by fwgen bridge. Do not edit.");
        text.AppendLine("using Godot;");
        text.AppendLine($"using {rootNamespace}.Core;");
        text.AppendLine("using GdArray = Godot.Collections.Array;");
        text.AppendLine("using GdDictionary = Godot.Collections.Dictionary;");
        text.AppendLine();
        text.AppendLine($"namespace {rootNamespace}.Bridge;");
        text.AppendLine();
        text.AppendLine("public static class PacketCodec");
        text.AppendLine("{");

        foreach (var payload in PacketPayloadFields(schema))
        {
            RenderPacketCreator(text, schema, payload);
            text.AppendLine();
        }

        foreach (var payload in PacketPayloadFields(schema))
        {
            RenderPacketReader(text, schema, payload);
            text.AppendLine();
        }

        text.AppendLine("    private static GdDictionary Payload(string field, Variant value)");
        text.AppendLine("    {");
        text.AppendLine("        var payload = new GdDictionary();");
        text.AppendLine("        payload[field] = value;");
        text.AppendLine("        return payload;");
        text.AppendLine("    }");
        text.AppendLine();
        text.AppendLine("    private static bool TryPayload(GdDictionary packet, string type, string field, out GdDictionary payload)");
        text.AppendLine("    {");
        text.AppendLine("        payload = new GdDictionary();");
        text.AppendLine("        if (!BridgeCodec.IsProtocolSupported(packet) || BridgeCodec.ReadType(packet) != type)");
        text.AppendLine("        {");
        text.AppendLine("            return false;");
        text.AppendLine("        }");
        text.AppendLine("        return BridgeCodec.TryGetDictionary(packet, field, out payload);");
        text.AppendLine("    }");
        text.AppendLine();
        text.AppendLine("    private static bool TryReadString(GdDictionary packet, string field, out string value)");
        text.AppendLine("    {");
        text.AppendLine("        value = \"\";");
        text.AppendLine("        if (!packet.TryGetValue(field, out Variant variant))");
        text.AppendLine("        {");
        text.AppendLine("            return false;");
        text.AppendLine("        }");
        text.AppendLine("        value = variant.AsString();");
        text.AppendLine("        return true;");
        text.AppendLine("    }");
        text.AppendLine();
        text.AppendLine("    private static bool TryReadBool(GdDictionary packet, string field, out bool value)");
        text.AppendLine("    {");
        text.AppendLine("        value = false;");
        text.AppendLine("        if (!packet.TryGetValue(field, out Variant variant))");
        text.AppendLine("        {");
        text.AppendLine("            return false;");
        text.AppendLine("        }");
        text.AppendLine("        value = variant.AsBool();");
        text.AppendLine("        return true;");
        text.AppendLine("    }");
        text.AppendLine();
        text.AppendLine("    private static bool TryReadFloat(GdDictionary packet, string field, out float value)");
        text.AppendLine("    {");
        text.AppendLine("        value = 0.0f;");
        text.AppendLine("        if (!packet.TryGetValue(field, out Variant variant))");
        text.AppendLine("        {");
        text.AppendLine("            return false;");
        text.AppendLine("        }");
        text.AppendLine("        value = (float)variant.AsDouble();");
        text.AppendLine("        return true;");
        text.AppendLine("    }");
        text.AppendLine();
        text.AppendLine("    private static bool TryReadVector2I(GdDictionary packet, string field, out Vector2I value)");
        text.AppendLine("    {");
        text.AppendLine("        value = Vector2I.Zero;");
        text.AppendLine("        if (!packet.TryGetValue(field, out Variant variant) || variant.VariantType != Variant.Type.Vector2I)");
        text.AppendLine("        {");
        text.AppendLine("            return false;");
        text.AppendLine("        }");
        text.AppendLine("        value = variant.AsVector2I();");
        text.AppendLine("        return true;");
        text.AppendLine("    }");
        text.AppendLine();
        text.AppendLine("    private static bool TryReadLong(GdDictionary packet, string field, out long value)");
        text.AppendLine("    {");
        text.AppendLine("        value = 0;");
        text.AppendLine("        if (!packet.TryGetValue(field, out Variant variant))");
        text.AppendLine("        {");
        text.AppendLine("            return false;");
        text.AppendLine("        }");
        text.AppendLine("        value = variant.AsInt64();");
        text.AppendLine("        return true;");
        text.AppendLine("    }");
        text.AppendLine();
        text.AppendLine("    private static bool TryReadInt(GdDictionary packet, string field, out int value)");
        text.AppendLine("    {");
        text.AppendLine("        value = 0;");
        text.AppendLine("        if (!packet.TryGetValue(field, out Variant variant))");
        text.AppendLine("        {");
        text.AppendLine("            return false;");
        text.AppendLine("        }");
        text.AppendLine("        value = (int)variant.AsInt64();");
        text.AppendLine("        return true;");
        text.AppendLine("    }");
        text.AppendLine("}");
        TextUtil.WriteText(output, text.ToString());
        Console.WriteLine($"generated bridge packet codec: {output}");
    }

    private static ProtoField[] PacketPayloadFields(ProtoSchema schema)
    {
        if (!schema.Messages.TryGetValue("Packet", out var packet))
        {
            return [];
        }
        return packet.Fields
            .Where(field => field.IsOneof)
            .ToArray();
    }

    private static void RenderPacketCreator(StringBuilder text, ProtoSchema schema, ProtoField payload)
    {
        var methodName = Pascal(payload.Name);
        var packetType = $"BridgePacketType.{methodName}";
        var payloadField = $"BridgeField.{methodName}";
        var payloadMessage = schema.Messages.TryGetValue(payload.Type, out var message)
            ? message
            : new ProtoMessage(payload.Type, "");
        var args = payloadMessage.Fields
            .Select(field => $"{PacketRuntimeType(field)} {Camel(field.Name)}")
            .ToArray();

        text.AppendLine($"    public static GdDictionary {methodName}({string.Join(", ", args)})");
        text.AppendLine("    {");
        text.AppendLine($"        var packet = BridgeCodec.Packet({packetType});");
        if (payloadMessage.Fields.Count == 0)
        {
            text.AppendLine($"        packet[{payloadField}] = new GdDictionary();");
        }
        else if (payloadMessage.Fields.Count == 1)
        {
            var field = payloadMessage.Fields[0];
            text.AppendLine($"        packet[{payloadField}] = Payload(BridgeField.{Pascal(field.Name)}, {Camel(field.Name)});");
        }
        else
        {
            text.AppendLine("        var payload = new GdDictionary();");
            foreach (var field in payloadMessage.Fields)
            {
                text.AppendLine($"        payload[BridgeField.{Pascal(field.Name)}] = {Camel(field.Name)};");
            }
            text.AppendLine($"        packet[{payloadField}] = payload;");
        }
        text.AppendLine("        return packet;");
        text.AppendLine("    }");
    }

    private static void RenderPacketReader(StringBuilder text, ProtoSchema schema, ProtoField payload)
    {
        var methodName = Pascal(payload.Name);
        var packetType = $"BridgePacketType.{methodName}";
        var payloadField = $"BridgeField.{methodName}";
        var payloadMessage = schema.Messages.TryGetValue(payload.Type, out var message)
            ? message
            : new ProtoMessage(payload.Type, "");

        if (payloadMessage.Fields.Count == 0)
        {
            text.AppendLine($"    public static bool TryRead{methodName}(GdDictionary packet)");
            text.AppendLine("    {");
            text.AppendLine($"        return TryPayload(packet, {packetType}, {payloadField}, out _);");
            text.AppendLine("    }");
            return;
        }

        var args = payloadMessage.Fields
            .Select(field => $"out {PacketRuntimeType(field)} {Camel(field.Name)}")
            .ToArray();
        text.AppendLine($"    public static bool TryRead{methodName}(GdDictionary packet, {string.Join(", ", args)})");
        text.AppendLine("    {");
        foreach (var field in payloadMessage.Fields)
        {
            text.AppendLine($"        {Camel(field.Name)} = {PacketDefaultValue(field)};");
        }
        text.AppendLine($"        if (!TryPayload(packet, {packetType}, {payloadField}, out GdDictionary payload))");
        text.AppendLine("        {");
        text.AppendLine("            return false;");
        text.AppendLine("        }");

        if (payloadMessage.Fields.Count == 1)
        {
            var field = payloadMessage.Fields[0];
            text.AppendLine($"        return {PacketReadExpression(field)};");
        }
        else
        {
            text.AppendLine("        var ok = true;");
            foreach (var field in payloadMessage.Fields)
            {
                text.AppendLine($"        ok &= {PacketReadExpression(field)};");
            }
            text.AppendLine("        return ok;");
        }
        text.AppendLine("    }");
    }

    private static string PacketRuntimeType(ProtoField field)
    {
        if (field.IsRepeated)
        {
            return "GdArray";
        }
        return field.Type switch
        {
            "string" => "string",
            "bool" => "bool",
            "float" or "double" => "float",
            "Vec2i" => "Vector2I",
            "PlayerId" => "long",
            "EntityId" => "int",
            "EntityKind" or "ItemKind" or "WeaponSlot" or "WeaponPhase" => "string",
            _ when IsIntLike(field.Type) => "int",
            _ => "GdDictionary",
        };
    }

    private static string PacketDefaultValue(ProtoField field)
    {
        if (field.IsRepeated)
        {
            return "new GdArray()";
        }
        return field.Type switch
        {
            "string" => "\"\"",
            "bool" => "false",
            "float" or "double" => "0.0f",
            "Vec2i" => "Vector2I.Zero",
            "PlayerId" => "0",
            "EntityId" => "0",
            "EntityKind" or "ItemKind" or "WeaponSlot" or "WeaponPhase" => BridgeEnumDefault(field.Type),
            _ when IsIntLike(field.Type) => "0",
            _ => "new GdDictionary()",
        };
    }

    private static string PacketReadExpression(ProtoField field)
    {
        var variable = Camel(field.Name);
        var bridgeField = $"BridgeField.{Pascal(field.Name)}";
        if (field.IsRepeated)
        {
            return $"BridgeCodec.TryGetArray(payload, {bridgeField}, out {variable})";
        }
        return field.Type switch
        {
            "string" => $"TryReadString(payload, {bridgeField}, out {variable})",
            "bool" => $"TryReadBool(payload, {bridgeField}, out {variable})",
            "float" or "double" => $"TryReadFloat(payload, {bridgeField}, out {variable})",
            "Vec2i" => $"TryReadVector2I(payload, {bridgeField}, out {variable})",
            "PlayerId" => $"TryReadLong(payload, {bridgeField}, out {variable})",
            "EntityId" => $"TryReadInt(payload, {bridgeField}, out {variable})",
            "EntityKind" or "ItemKind" or "WeaponSlot" or "WeaponPhase" => $"TryReadString(payload, {bridgeField}, out {variable})",
            _ when IsIntLike(field.Type) => $"TryReadInt(payload, {bridgeField}, out {variable})",
            _ => $"BridgeCodec.TryGetDictionary(payload, {bridgeField}, out {variable})",
        };
    }

    private static void RenderEventEncoder(StringBuilder text, ProtoSchema schema)
    {
        text.AppendLine("    private static GdDictionary EncodeOne(CoreEvent ev)");
        text.AppendLine("    {");
        text.AppendLine("        var data = new GdDictionary();");
        text.AppendLine("        data[BridgeField.Type] = ev.Type;");
        text.AppendLine("        switch (ev.Type)");
        text.AppendLine("        {");
        if (schema.Messages.TryGetValue("GameEvent", out var eventRoot))
        {
            foreach (var variant in eventRoot.Fields.Where(item => item.IsOneof))
            {
                if (!schema.Messages.TryGetValue(variant.Type, out var message))
                {
                    continue;
                }
                text.AppendLine($"            case CoreEvent.{ClassNameForEvent(variant.Type)}:");
                foreach (var field in message.Fields)
                {
                    text.AppendLine($"                data[BridgeField.{Pascal(field.Name)}] = ev.{Pascal(field.Name)};");
                }
                text.AppendLine("                break;");
            }
        }
        text.AppendLine("        }");
        text.AppendLine("        return data;");
        text.AppendLine("    }");
    }

    private static void RenderBridgeFields(StringBuilder text, ProtoSchema schema)
    {
        var fields = schema.Messages.Values
            .SelectMany(message => message.Fields)
            .Select(field => field.Name)
            .Append("kind")
            .Append("type")
            .Distinct(StringComparer.Ordinal)
            .OrderBy(item => item, StringComparer.Ordinal)
            .ToArray();

        text.AppendLine("public static class BridgeField");
        text.AppendLine("{");
        foreach (var field in fields)
        {
            text.AppendLine($"    public const string {Pascal(field)} = \"{field}\";");
        }
        text.AppendLine("}");
    }

