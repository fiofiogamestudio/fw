                    ? value.Name["PACKET_TYPE_".Length..].ToLowerInvariant()
                    : EnumTail(value.Name);
                text.AppendLine($"    public const string {Pascal(name)} = \"{name}\";");
            }
        }
        text.AppendLine("}");
    }

