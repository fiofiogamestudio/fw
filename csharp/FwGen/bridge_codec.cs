    private static void GenerateIntentCodecCs(
        GenerationBatch batch,
        string root,
        FwConfig config,
        BridgeModel model
    )
    {
        var schema = model.Schema;
        var output = config.BridgeIntentCodecCsPath(root);
        var intentRoot = model.IntentRoot
            ?? throw new InvalidOperationException("intent.proto has no root message");
        var intentFields = intentRoot.Fields.Where(item => !item.IsOneof).OrderBy(item => item.Number).ToArray();
        var playerIdField = intentFields.FirstOrDefault(field => field.Type == "PlayerId" || field.Name == "player_id");
        var rootNamespace = TextUtil.PascalName(config.ProjectName());
        var text = new StringBuilder();
        text.AppendLine("// @generated by fwgen bridge. Do not edit.");
        text.AppendLine("using System.Collections.Generic;");
        text.AppendLine("using Godot;");
        text.AppendLine($"using {rootNamespace}.Core;");
        text.AppendLine("using GdArray = Godot.Collections.Array;");
        text.AppendLine("using GdDictionary = Godot.Collections.Dictionary;");
        text.AppendLine();
        text.AppendLine($"namespace {rootNamespace}.Bridge;");
        text.AppendLine();
        text.AppendLine("public static class IntentCodec");
        text.AppendLine("{");
        text.AppendLine($"    public static List<{intentRoot.Name}> Decode(GdArray intents)");
        text.AppendLine("    {");
        text.AppendLine($"        var intentsOut = new List<{intentRoot.Name}>();");
        text.AppendLine("        foreach (Variant intent in intents)");
        text.AppendLine("        {");
        text.AppendLine("            if (intent.VariantType != Variant.Type.Dictionary)");
        text.AppendLine("            {");
        text.AppendLine("                continue;");
        text.AppendLine("            }");
        text.AppendLine();
        text.AppendLine("            GdDictionary dict = intent.AsGodotDictionary();");
        var playerIdRead = playerIdField == null
            ? "-1"
            : $"BridgeCodec.ReadLong(dict, BridgeField.{Pascal(playerIdField.Name)}, -1)";
        text.AppendLine($"            intentsOut.Add(DecodeOne({playerIdRead}, dict));");
        text.AppendLine("        }");
        text.AppendLine("        return intentsOut;");
        text.AppendLine("    }");
        text.AppendLine();
        text.AppendLine($"    public static {intentRoot.Name} DecodeOne(long playerId, GdDictionary dict)");
        text.AppendLine("    {");
        text.AppendLine($"        return new {intentRoot.Name}(");
        for (var index = 0; index < intentFields.Length; index++)
        {
            var field = intentFields[index];
            var expression = IntentDecodeExpression(schema, field, playerIdField, model.ActionRoot);
            var suffix = index + 1 == intentFields.Length ? "" : ",";
            text.AppendLine($"            {expression}{suffix}");
        }
        text.AppendLine("        );");
        text.AppendLine("    }");
        text.AppendLine();
        RenderInputActionDecoder(text, model);
        text.AppendLine("}");
        batch.StageText(output, text.ToString());
        batch.StageDelete(Path.Combine(Path.GetDirectoryName(output) ?? root, "_input_codec.cs"));
    }

    private static void RenderInputActionDecoder(StringBuilder text, BridgeModel model)
    {
        var schema = model.Schema;
        var actionRoot = model.ActionRoot;
        var intentRoot = model.IntentRoot;
        if (actionRoot == null || intentRoot == null)
        {
            return;
        }
        var actionField = intentRoot.Fields.First(field => field.Type == actionRoot.Name);
        text.AppendLine($"    private static {actionRoot.Name}? DecodeAction(GdDictionary dictionary)");
        text.AppendLine("    {");
        text.AppendLine($"        if (!BridgeCodec.TryGetDictionary(dictionary, BridgeField.{Pascal(actionField.Name)}, out GdDictionary action))");
        text.AppendLine("        {");
        text.AppendLine("            return null;");
        text.AppendLine("        }");
        text.AppendLine();
        text.AppendLine("        string kind = BridgeCodec.ReadString(action, BridgeField.Kind);");
        text.AppendLine("        if (string.IsNullOrEmpty(kind))");
        text.AppendLine("        {");
        text.AppendLine("            return null;");
        text.AppendLine("        }");
        text.AppendLine();
        text.AppendLine($"        return new {actionRoot.Name}");
        text.AppendLine("        {");
        text.AppendLine("            Kind = kind,");
        text.AppendLine("            Payload = DecodeActionPayload(kind, action),");
        foreach (var field in model.ActionFields)
        {
            text.AppendLine($"            {Pascal(field.Name)} = {ReadExpression(schema, "action", field)},");
        }
        text.AppendLine("        };");
        text.AppendLine("    }");
        text.AppendLine();
        text.AppendLine("    private static object? DecodeActionPayload(string kind, GdDictionary action)");
        text.AppendLine("    {");
        text.AppendLine("        return kind switch");
        text.AppendLine("        {");
        foreach (var variant in actionRoot.Fields.Where(item => item.IsOneof))
        {
            if (!schema.Messages.TryGetValue(variant.Type, out var payload))
            {
                continue;
            }
            text.AppendLine($"            {actionRoot.Name}.{Pascal(variant.Name)} => new {payload.Name}");
            text.AppendLine("            {");
            foreach (var field in payload.Fields)
            {
                text.AppendLine($"                {Pascal(field.Name)} = {ReadExpression(schema, "action", field)},");
            }
            text.AppendLine("            },");
        }
        text.AppendLine("            _ => null,");
        text.AppendLine("        };");
        text.AppendLine("    }");
    }

    private static string ReadExpression(ProtoSchema schema, string source, ProtoField field)
    {
        var bridgeField = $"BridgeField.{Pascal(field.Name)}";
        if (field.IsRepeated)
        {
            return $"BridgeCodec.TryGetArray({source}, {bridgeField}, out GdArray {field.Name}Array) ? {field.Name}Array : new GdArray()";
        }
        return field.Type switch
        {
            "string" => $"BridgeCodec.ReadString({source}, {bridgeField})",
            "bool" => $"BridgeCodec.ReadBool({source}, {bridgeField}, false)",
            "float" or "double" => $"BridgeCodec.ReadFloat({source}, {bridgeField}, 0.0f)",
            "Vec2i" => $"BridgeCodec.ReadVector2I({source}, {bridgeField}, Vector2I.Zero)",
            "PlayerId" => $"BridgeCodec.ReadLong({source}, {bridgeField}, {ReadFallback(field)})",
            _ when schema.Enums.ContainsKey(field.Type) => $"BridgeCodec.ReadString({source}, {bridgeField}, {BridgeEnumDefault(schema, field.Type)})",
            _ when IsIntLike(field.Type) => $"(int)BridgeCodec.ReadLong({source}, {bridgeField}, {ReadFallback(field)})",
            _ => $"BridgeCodec.ReadString({source}, {bridgeField})",
        };
    }

    private static string IntentDecodeExpression(
        ProtoSchema schema,
        ProtoField field,
        ProtoField? playerIdField,
        ProtoMessage? actionRoot
    )
    {
        if (playerIdField != null && field.Name == playerIdField.Name)
        {
            return "playerId";
        }
        if (actionRoot != null && field.Type == actionRoot.Name)
        {
            return "DecodeAction(dict)";
        }
        if (field.Name == "client_tick" && field.Type == "uint32")
        {
            return $"(uint)BridgeCodec.ReadLong(dict, BridgeField.{Pascal(field.Name)}, 0)";
        }
        return ReadExpression(schema, "dict", field);
    }

    private static string ReadFallback(ProtoField field)
    {
        _ = field;
        return "0";
    }

